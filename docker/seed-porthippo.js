/**
 * seed-porthippo.js — populate Port Hippo's store with definitions that match the
 * Docker sandbox (see docker/README.md): two reusable credentials (key + password),
 * a jump host, and two tunnels (direct to the jump's echo, and through the jump to
 * the dest's echo).
 *
 * Runs through the REAL store layer (src/app/store), so secrets are sealed with the
 * app's own at-rest crypto (the app-key file already in the data dir) and every
 * record is validated + referentially checked exactly as the UI would do it.
 *
 * Idempotent: records are keyed by label/name, so re-running only adds what's
 * missing. Never touches unrelated entries you already have.
 *
 *   node docker/seed-porthippo.js [dataDir]
 *   PORTHIPPO_DATA_DIR=/path node docker/seed-porthippo.js
 *
 * Default dataDir is the repo's `data/` dir — the one `make debug` uses.
 */
"use strict";

const fs = require("node:fs");
const path = require("node:path");

const { Stores } = require(
  path.join(__dirname, "..", "src", "app", "store", "stores"),
);

// ── Config (read from docker/.env so it never drifts from the compose file) ──────
function readEnv(file) {
  const env = {};
  if (!fs.existsSync(file)) return env;
  for (const line of fs.readFileSync(file, "utf8").split("\n")) {
    const m = line.match(/^\s*([A-Z0-9_]+)\s*=\s*(.*)\s*$/);
    if (m) env[m[1]] = m[2];
  }
  return env;
}

const env = readEnv(path.join(__dirname, ".env"));
const SSH_USER = env.SSH_USER || "tunnel";
const SSH_PASSWORD = env.SSH_PASSWORD || "tunnelpass";
const JUMP_SSH_PORT = Number(env.JUMP_SSH_PORT || 2201);
const DEST_BACK_IP = env.DEST_BACK_IP || "172.29.0.12";
const ECHO_PORT = Number(env.ECHO_PORT || 7000);
const NET_ECHO_PORT = Number(env.NET_ECHO_PORT || 7001);
// Feature 110 new-type test parameters.
const SOCKS_LOCAL_PORT = Number(env.SOCKS_LOCAL_PORT || 18080);
const REMOTE_BIND_PORT = Number(env.REMOTE_BIND_PORT || 9090);
const HOST_ECHO_PORT = Number(env.HOST_ECHO_PORT || 9091);
const KEY_PATH = path.join(__dirname, "keys", "id_porthippo");

const dataDir =
  process.argv[2] ||
  process.env.PORTHIPPO_DATA_DIR ||
  path.join(__dirname, "..", "data");

// ── Idempotent upserts ──────────────────────────────────────────────────────────
const stores = new Stores(dataDir);
const creds = stores.credentialStore();
const jumps = stores.jumpHostStore();
const tunnels = stores.tunnelStore();

let added = 0;

function ensureCredential(spec) {
  const existing = creds.list().find((c) => c.label === spec.label);
  if (existing) {
    console.log(`  = credential "${spec.label}" already exists`);
    return existing.id;
  }
  const created = creds.create(spec);
  added++;
  console.log(`  + credential "${spec.label}" (${spec.authType})`);
  return created.id;
}

function ensureJumpHost(spec) {
  const existing = jumps.list().find((j) => j.label === spec.label);
  if (existing) {
    console.log(`  = jump host "${spec.label}" already exists`);
    return existing.id;
  }
  const created = jumps.create(spec);
  added++;
  console.log(`  + jump host "${spec.label}" (${spec.host}:${spec.port})`);
  return created.id;
}

function ensureTunnel(spec) {
  const existing = tunnels.list().find((t) => t.name === spec.name);
  if (existing) {
    console.log(`  = tunnel "${spec.name}" already exists`);
    return existing.id;
  }
  const created = tunnels.create(spec);
  added++;
  console.log(`  + tunnel "${spec.name}"  →  ${created.routeSummary || ""}`);
  return created.id;
}

console.log(`Seeding Port Hippo sandbox definitions into: ${dataDir}\n`);

// Two reusable credentials — the same login, exercised both ways.
const keyCredId = ensureCredential({
  label: "Sandbox key (tunnel)",
  user: SSH_USER,
  authType: "key",
  keyPath: KEY_PATH, // passphrase-less key generated by `make sandbox-create`
});
const pwCredId = ensureCredential({
  label: "Sandbox password (tunnel)",
  user: SSH_USER,
  authType: "password",
  password: SSH_PASSWORD, // sealed at rest by the store
});

// The jump host (bastion) — reached directly from this machine. Authenticates by
// PASSWORD, to show that hop using a different method than the final one.
const jumpId = ensureJumpHost({
  label: "Docker jump (127.0.0.1)",
  host: "127.0.0.1",
  port: JUMP_SSH_PORT,
  credentialId: pwCredId,
});

// Scenario A (LOCAL) — SSH straight to the jump host, forward to ITS loopback echo.
// sshHost = the jump (127.0.0.1:2201); destination = the jump's loopback echo.
ensureTunnel({
  name: "Sandbox — jump echo (direct)",
  type: "local",
  localPort: 18001,
  destination: { host: "127.0.0.1", port: ECHO_PORT },
  sshHost: "127.0.0.1",
  sshPort: JUMP_SSH_PORT, // SSH server = the jump host (127.0.0.1:2201)
  credentialId: keyCredId,
  enabled: true,
});

// Scenario B (LOCAL) — through the jump host to the DEST box, forward to ITS
// loopback echo. Non-blank sshHost ⇒ bastion: SSH to sshHost (dest) via the jump
// chain, then forward to destination host:port. Final hop uses KEY auth.
ensureTunnel({
  name: "Sandbox — dest echo (via jump)",
  type: "local",
  localPort: 18002,
  destination: { host: "127.0.0.1", port: ECHO_PORT },
  sshHost: DEST_BACK_IP, // final SSH server = dest
  sshPort: 22,
  jumpHostIds: [jumpId],
  credentialId: keyCredId,
  enabled: true,
});

// Scenario C (DYNAMIC / SOCKS, Feature 110) — a local SOCKS5 proxy that exits at
// the jump host. Point a browser/app at 127.0.0.1:<SOCKS_LOCAL_PORT> and it can
// reach any host the jump can — including the SEALED dest at its network echo
// (${DEST_BACK_IP}:${NET_ECHO_PORT}), which nothing on the host can reach directly.
ensureTunnel({
  name: "Sandbox — SOCKS proxy (via jump)",
  type: "dynamic",
  localPort: SOCKS_LOCAL_PORT,
  sshHost: "127.0.0.1",
  sshPort: JUMP_SSH_PORT, // the SOCKS exit vantage = the jump host
  credentialId: keyCredId,
  enabled: true,
});

// Scenario D (REMOTE / reverse, Feature 110) — bind a port ON the jump host and
// forward every inbound connection back to a local echo on THIS machine. Run
// `make sandbox-host-echo` first (the local target), arm this tunnel, then from
// the jump: `docker exec porthippo-jump sh -c \
//   'echo ping | socat - TCP:127.0.0.1:${REMOTE_BIND_PORT}'` — it reaches the host echo.
ensureTunnel({
  name: "Sandbox — reverse forward (jump → host)",
  type: "remote",
  remoteBind: { host: "127.0.0.1", port: REMOTE_BIND_PORT }, // bound on the jump
  destination: { host: "127.0.0.1", port: HOST_ECHO_PORT }, // the host-side echo
  sshHost: "127.0.0.1",
  sshPort: JUMP_SSH_PORT,
  credentialId: keyCredId,
  enabled: true,
});

console.log(
  `\nDone. ${added} record(s) added. ` +
    `Run 'make debug' and open the Definition view.`,
);
